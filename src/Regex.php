<?php

namespace Diffhead\PHP\Url;

enum Regex: string
{
    case Scheme = '/^(\w+):\/\//';
    case Hostname = '/^(?:(?:[A-Za-z][A-Za-z0-9+\-.]*):\/\/)?(?:(?:[A-Za-z0-9\-._~!$&\'()*+,;=:]|%[0-9A-Fa-f]{2})*@)?(\[(?:[0-9A-Fa-f:.]+|v[0-9A-Fa-f]+\.[A-Za-z0-9\-._~!$&\'()*+,;=:]+)\]|(?:[A-Za-z0-9\-._~]|%[0-9A-Fa-f]{2}|[!$&\'()*+,;=])+)(?=[:\/?#]|$)/';
    case Port = '/^(\w+:\/\/){0,1}[\w\d]+[\w\d\.-_]{0,}?:?(\d+)[\/]?/';
    case Path = '/^(?:[a-z][a-z0-9+\-.]*:\/\/)?[^\/\?#]+(?:\:\d+)?(\/[^?\#]*)?/';
    case Query = '/\?(.*?)?(?=#|$)/';
    case QueryArrayKeys = '/\[([^\]]*)\]/';
}
